<?php

namespace AleBatistella\BlingErpApi\Entities\Usuarios\Schema\ValidateHash;

use AleBatistella\BlingErpApi\Entities\Shared\BaseResponseRootObject;
use AleBatistella\BlingErpApi\Entities\Shared\DTO\Request\ResponseOptions;

/**
 * Resposta da validação do hash recebido por e-mail.
 */
readonly final class ValidateHashResponse extends BaseResponseRootObject
{
    /**
     * Constrói o objeto.
     *
     * @param ValidateHashResponseData[] $data
     */
    public function __construct(
        public array $data
    ) {
    }

    /**
     * @inheritDoc
     */
    protected static function fromRules(): array
    {
        return [
            'data' => ValidateHashResponseData::class
        ];
    }

    /**
     * @inheritDoc
     */
    public static function fromResponse(ResponseOptions $response): static
    {
        if (is_null($response->body?->content)) {
            static::throwForInconsistentResponseOptions($response);
        }

        return self::from($response->body->content);
    }
}
